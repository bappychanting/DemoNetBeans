    public void currentDateTime(){
        Calendar cal = new GregorianCalendar();
        int month = cal.get(Calendar.MONTH);
        int year = cal.get(Calendar.YEAR);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        int minute = cal.get(Calendar.MINUTE);
        int hour = cal.get(Calendar.HOUR_OF_DAY);
        String dayTime = "";
        if (cal.get(Calendar.AM_PM) == 0) {
            dayTime = "AM";
        } else {
            dayTime = "PM";
        }
        menu_date.setText(day+"/"+(month+1)+"/"+year+" "+hour+":"+minute+" "+dayTime);
    }
    
    private void updateTable(){
        String sql = "Select id, Name, Surname, username, Age from Employee";
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            table_employee.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                pst.close();
                rs.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }  
        }
    }
    
    private void updateCombo(){
        String sql = "Select username from Employee";
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            combo_name.removeAllItems();
            combo_name.addItem("---Select Employee---");
            while(rs.next()){
                String name = rs.getString("username");
                combo_name.addItem(name);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                pst.close();
                rs.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }  
        }
    }
    
    private void getTableData(String row){
        String sql = "Select * from Employee where id = '"+row+"' ";
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                String get_id = rs.getString("id");
                label_id.setText(get_id);
                String get_username = rs.getString("username");
                txt_username.setText(get_username);
                String get_name = rs.getString("name");
                txt_name.setText(get_name);
                String get_surname = rs.getString("surname");
                txt_surname.setText(get_surname);
                String get_age = rs.getString("age");
                txt_age.setText(get_age);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                pst.close();
                rs.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }  
        }
    }
    
