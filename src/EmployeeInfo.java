    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try{
            if(label_id.getText().isEmpty()){
                String get_last_id = "Select MAX(id) as id from Employee";
                pst = conn.prepareStatement(get_last_id);
                rs = pst.executeQuery();
                if(rs.next()){
                    Integer last_id = Integer.parseInt(rs.getString("id")) + 1;
                    label_id.setText(Integer.toString(last_id));
                }
                String sql = "Insert into Employee (Name, Surname, username, Age) values (? , ? , ?, ?)";
                pst = conn.prepareStatement(sql);
                pst.setString(1, txt_name.getText());
                pst.setString(2, txt_surname.getText());
                pst.setString(3, txt_username.getText());
                pst.setString(4, txt_age.getText());
            }
            else{
                String sql = "Update Employee set Name = ?, Surname =?, username=?, Age=? where id=?";
                pst = conn.prepareStatement(sql);
                pst.setString(1, txt_name.getText());
                pst.setString(2, txt_surname.getText());
                pst.setString(3, txt_username.getText());
                pst.setString(4, txt_age.getText());
                pst.setString(5, label_id.getText());
            }
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Saved!");
            updateTable();
            updateCombo();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                pst.close();
                rs.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }  
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(label_id.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No employee selected!");
        }
        else{
            int p =  JOptionPane.showConfirmDialog(null, "Do you really want to remove this employee?", "Delete", JOptionPane.YES_NO_OPTION);
            if(p == 0){
                String sql = "Delete from Employee where id = ?";
                try{
                    pst = conn.prepareStatement(sql);
                    pst.setString(1, label_id.getText());
                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Deleted!");
                    textEmpty();
                    updateTable();
                    updateCombo();
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
                finally{
                    try{
                        pst.close();
                        rs.close();
                    }
                    catch(Exception e){
                        JOptionPane.showMessageDialog(null, e);
                    }  
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

